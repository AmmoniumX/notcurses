static int
enhalfen(const char* s, int width){
  if(strlen(s) % (width * 2)){
    fprintf(stderr, "Bad length: %zu\n", strlen(s));
    return -1;
  }
  size_t rowoff = 0;
  size_t idx;
  for(rowoff = 0 ; s[rowoff] ; rowoff += 2 * width){ 
    for(idx = 0 ; idx < width ; ++idx){
      int c = (s[rowoff + idx] - '0') * 4 + (s[rowoff + width + idx] - '0');
      printf("%x", c);
    }
    printf("\n");
  }
  return 0;
}

